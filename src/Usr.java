
public class Usr {
    public String login;
    public String phonenum;
    public String password;
    public String status;
    public String block_list;
    public String contact_list;

    public Usr(String login, String phonenum, String password, String status, String block_list, String contact_list) {
        this.login = login;
        this.phonenum = phonenum;
        this.password = password;
        this.status = status;
        this.block_list = block_list;
        this.contact_list = contact_list;
    }
}
